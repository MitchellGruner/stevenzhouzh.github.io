$("#work_rna_b").click(function() {
    if ($("#work_rna_p").hasClass("hidden")) {
        $(".accordian_body, .dropdown_arrow_expanded").addClass("hidden");
        $(".dropdown_arrow").removeClass("hidden");

        $("#work_rna_b > .dropdown_arrow").addClass("hidden");
        $("#work_rna_b > .dropdown_arrow_expanded, #work_rna_p").removeClass("hidden");
      } else {
        $(".accordian_body, .dropdown_arrow_expanded").addClass("hidden");
        $(".dropdown_arrow").removeClass("hidden");
      }
  });

